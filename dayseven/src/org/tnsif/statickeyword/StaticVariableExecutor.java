package org.tnsif.statickeyword;
/*static variable if any variable is non-static and outside
 * the main function if you want to
 * access that variable inside the main function, then we can make
 * that variable as static*/
public class StaticVariableExecutor {
	static int num=90;
	

	public static void main(String[] args) {	
		System.out.println(num);

	}

}
