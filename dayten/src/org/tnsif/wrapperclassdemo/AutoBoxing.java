/*
 * Program to demonstrate on Auto-Boxing.
 * Auto-Boxing:- Conversion of Primitive type to Object type. 
*/
package org.tnsif.wrapperclassdemo;

public class AutoBoxing {

	public static void main(String[] args) {
		char c = 'A';
		
		// Converting a Primitive Data Type into object type.
		Character ch = c;
		System.out.println(ch);
			
	}
}
